package com.sohail.transit.Models;

public class InfoWindowModel {

    String stopTxt,congestionTxt,incomingTxt;

    public InfoWindowModel() {
    }

    public String getStopTxt() {
        return stopTxt;
    }

    public void setStopTxt(String stopTxt) {
        this.stopTxt = stopTxt;
    }

    public String getCongestionTxt() {
        return congestionTxt;
    }

    public void setCongestionTxt(String congestionTxt) {
        this.congestionTxt = congestionTxt;
    }

    public String getIncomingTxt() {
        return incomingTxt;
    }

    public void setIncomingTxt(String incomingTxt) {
        this.incomingTxt = incomingTxt;
    }
}
